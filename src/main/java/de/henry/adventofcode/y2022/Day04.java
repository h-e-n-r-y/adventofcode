    protected static int[] calcNrOfOverlappingPairs(String pInput) {
        int sumFullOverlaps = 0;
        int sumPartialOverlaps = 0;
        for (String l : pInput.split("\n")) {
            String[] pair = l.split(",");
            Range r1 = new Range(pair[0]);
            Range r2 = new Range(pair[1]);
            if (isFullOverlap(r1, r2)) {
                sumFullOverlaps++;
                sumPartialOverlaps++;
            } else {
                if (r1.overlaps(r2)) {
                    sumPartialOverlaps++;
                }
            }
        }
        int[] r = new int[2];
        r[0] = sumFullOverlaps;
        r[1] = sumPartialOverlaps;
        return r;
    }

    private static boolean isFullOverlap(Range r1, Range r2) {
        return r1.fullyContains(r2) || r2.fullyContains(r1);
    }

    static class Range {
        int from;
        int to;

        Range(String pRange) {
            int m = pRange.indexOf('-');
            from = Integer.parseInt(pRange.substring(0, m));
            to = Integer.parseInt(pRange.substring(m+1));
        }

        public boolean fullyContains(Range r2) {
            return from <= r2.from && to >= r2.to;
        }
        public boolean overlaps(Range r2) {
            return to >= r2.from && to <= r2.to || r2.to >= from && r2.to <= to;
        }
    }

